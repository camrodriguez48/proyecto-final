    private void calcular_lcrpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcular_lcrpActionPerformed
double sueldo_neto;
double tardanza; 
double falta; 
double sueldo_bruto;

sueldo_bruto= Double.parseDouble(horas_trabajo_lcrp.getText())*30;
tardanza= Double.parseDouble(tardanzas_lcrp.getText())*30;
falta= Double.parseDouble(faltas_lcrp.getText())*9;
sueldo_neto=sueldo_bruto-tardanza-falta;

tex1_lcrp.setText( "INFORMACION DEL EMPLEADO \n" +
                   "Sueldo Bruto: " + sueldo_bruto + " $ \n" +
                  "Des por Tardanza: " + tardanza + " $ \n" +
                  "Des Por falta: " + falta + " $ \n" +
                  "Sueldo Neto: " + sueldo_neto + " $");


    }//GEN-LAST:event_calcular_lcrpActionPerformed

